-- 1. Extend public.articles
ALTER TABLE public.articles
ADD COLUMN IF NOT EXISTS author text DEFAULT 'Admin',
ADD COLUMN IF NOT EXISTS sub_headline text,
ADD COLUMN IF NOT EXISTS region text DEFAULT 'Punjab',
ADD COLUMN IF NOT EXISTS seo_title text,
