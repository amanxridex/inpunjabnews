-- 7. RPC Function to atomically increment view count
CREATE OR REPLACE FUNCTION increment_view_count(article_id bigint)
RETURNS void AS $$
BEGIN
  UPDATE public.articles
  SET view_count = coalesce(view_count, 0) + 1
  WHERE id = article_id;
END;
$$ LANGUAGE plpgsql;

-- 8. RPC Function to atomically increment ad clicks
CREATE OR REPLACE FUNCTION increment_ad_clicks(ad_id bigint)
RETURNS void AS $$
BEGIN
  UPDATE public.ads
  SET clicks = coalesce(clicks, 0) + 1
  WHERE id = ad_id;
END;
$$ LANGUAGE plpgsql;

